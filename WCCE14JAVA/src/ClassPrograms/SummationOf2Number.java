package ClassPrograms;

public class SummationOf2Number
{
	public static void main(String[] args) 
	{
		
	//  summation  	
		int a=10;
		int b=20;
		int sum=a+b;
		System.out.println("the summation of a&b:"+sum);
	
		//substriction
		int p=10;
		int q=20;
		int sub=p-q;
		System.out.println("the sub of p&q:"+sub);
		
		//Multipication 
		int c=10;
		int d=20;
		int mul=c*d;
		System.out.println("the mul of c&d:"+mul);
		
		//division
		int e=10;
		int f=20;
		int div=e/f;
		System.out.println("the div of e&f:"+div);
		
		//modulas
		int g=10;
		int h=20;
		int mod=e%f;
		System.out.println("the mod of g&h:"+mod);		
	}
}
